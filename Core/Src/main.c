	LCD_String(lcd_str);

	temperature = 56;
	adc_value = 1.8;
	LCD_SetPos(0, 1);	// Перевод курсора на 0 символ 1 строки
	sprintf(lcd_str, "%3ddg %2.1fv", temperature, adc_value);
